
# While modules are initialized by code within the module file itself, 
# packages do not have this option since they are directories. 
#
# So Python expects there to be a file called init.py inside the package 
# folder, and the contents of that file are executed when any module in 
# the package is imported. 
#
# If no special initializations are needed, the init.py 
# file can be left empty, but it must not be omitted.
#
